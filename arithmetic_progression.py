"""
–ó–∞–¥–∞—á–∞ 1. –î–∞–Ω–æ –ø–µ—Ä—à–∏–π —á–ª–µ–Ω —Ç–∞ —Ä—ñ–∑–Ω–∏—Ü—è –∞—Ä–∏—Ñ–º–µ—Ç–∏—á–Ω–æ—ó –ø—Ä–æ–≥—Ä–µ—Å—ñ—ó.
–ù–∞–ø–∏—Å–∞—Ç–∏ —Ä–µ–∫—É—Ä—Å–∏–≤–Ω—É —Ñ—É–Ω–∫—Ü—ñ—é –¥–ª—è –æ–±—á–∏—Å–ª–µ–Ω–Ω—è ùëõ-–≥–æ —á–ª–µ–Ω–∞ –ø—Ä–æ–≥—Ä–µ—Å—ñ—ó
—Ç–∞ —Å—É–º–∏ –ø–µ—Ä—à–∏—Ö ùëõ —á–∏—Å–µ–ª.

"""

def nth_term(a1, d, n):
    
    if n == 1:
        return a1
    return nth_term(a1, d, n - 1) + d


def sum_progression(a1, d, n):
    
    if n == 1:
        return a1
    return nth_term(a1, d, n) + sum_progression(a1, d, n - 1)


# -------- –û—Å–Ω–æ–≤–Ω–∞ –ø—Ä–æ–≥—Ä–∞–º–∞ --------
a1 = float(input("–í–≤–µ–¥—ñ—Ç—å –ø–µ—Ä—à–∏–π —á–ª–µ–Ω –ø—Ä–æ–≥—Ä–µ—Å—ñ—ó a1: "))
d = float(input("–í–≤–µ–¥—ñ—Ç—å —Ä—ñ–∑–Ω–∏—Ü—é –ø—Ä–æ–≥—Ä–µ—Å—ñ—ó d: "))
n = int(input("–í–≤–µ–¥—ñ—Ç—å –Ω–æ–º–µ—Ä —á–ª–µ–Ω–∞ n: "))

an = nth_term(a1, d, n)
s = sum_progression(a1, d, n)

print("n-–π —á–ª–µ–Ω –∞—Ä–∏—Ñ–º–µ—Ç–∏—á–Ω–æ—ó –ø—Ä–æ–≥—Ä–µ—Å—ñ—ó:", an)
print("–°—É–º–∞ –ø–µ—Ä—à–∏—Ö n —á–ª–µ–Ω—ñ–≤ –ø—Ä–æ–≥—Ä–µ—Å—ñ—ó:", s)
